package com.example.t3_01.Entities;

public class Animes {
    public int id;
    public int img;
    public String name;
    public String description;

    public Animes() {
    }

    public Animes(int id, int img, String name, String description) {
        this.id = id;
        this.img = img;
        this.name = name;
        this.description = description;
    }
}
